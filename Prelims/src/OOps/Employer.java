package OOps;

public class Employer {
	string name;
	int salary;
	int empID;
	public string getName() {
		return name;
	}
	public void setName(string name) {
		this.name = name;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public int getEmpID() {
		return empID;
	}
	public void setEmpID(int empID) {
		this.empID = empID;
	}
	

}
